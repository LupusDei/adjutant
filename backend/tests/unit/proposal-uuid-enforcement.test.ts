// adj-141.4: Event bus emissions use UUID-format projectId
// =============================================================================

describe("event bus emissions use UUID projectId (adj-141.4)", () => {
  const SESSION_ID = "session-456";
  const PROJECT_CONTEXT = {
    projectId: VALID_UUID,
    projectName: "adjutant",
    projectPath: "/code/adjutant",
    beadsDir: "/code/adjutant/.beads",
  };

  beforeEach(() => {
    vi.clearAllMocks();
    mockGetAgentBySession.mockReturnValue("reviewer-agent");
    mockGetProjectContextBySession.mockReturnValue(PROJECT_CONTEXT);
    mockResolveToolProjectContext.mockReturnValue(PROJECT_CONTEXT);
    mockGetProject.mockReturnValue({
      success: true,
      data: { id: VALID_UUID, name: "adjutant", autoDevelop: true },
    });
    mockEnableAutoDevelop.mockReturnValue({ success: true });
    mockDisableAutoDevelop.mockReturnValue({ success: true });
    mockEmit.mockClear();
  });

  it("should emit proposal:scored with UUID projectId (not proposal.project)", async () => {
    const store = createMockStore();
    const handler = getAutoDevelopToolHandler(store, "score_proposal");

    await handler(
      {
        proposalId: "existing-proposal-uuid",
        score: 0.85,
        classification: "accept",
        signals: { feasibility: 0.9, impact: 0.8 },
      },
      { sessionId: SESSION_ID },
    );

    const scoredCall = mockEmit.mock.calls.find(
      (c: unknown[]) => c[0] === "proposal:scored",
    );
    expect(scoredCall).toBeDefined();
    const eventData = scoredCall![1] as { projectId: string };
    expect(eventData.projectId).toBe(VALID_UUID);
    expect(UUID_RE.test(eventData.projectId)).toBe(true);
  });

  it("should emit project:auto_develop_enabled with UUID projectId", async () => {
    const store = createMockStore();
    const handler = getAutoDevelopToolHandler(store, "enable_auto_develop");

    await handler({}, { sessionId: SESSION_ID });

    const enabledCall = mockEmit.mock.calls.find(
      (c: unknown[]) => c[0] === "project:auto_develop_enabled",
    );
    expect(enabledCall).toBeDefined();
    const eventData = enabledCall![1] as { projectId: string };
    expect(eventData.projectId).toBe(VALID_UUID);
    expect(UUID_RE.test(eventData.projectId)).toBe(true);
  });

  it("should emit project:auto_develop_disabled with UUID projectId", async () => {
    const store = createMockStore();
    const handler = getAutoDevelopToolHandler(store, "disable_auto_develop");

    await handler({}, { sessionId: SESSION_ID });

    const disabledCall = mockEmit.mock.calls.find(
      (c: unknown[]) => c[0] === "project:auto_develop_disabled",
    );
    expect(disabledCall).toBeDefined();
    const eventData = disabledCall![1] as { projectId: string };
    expect(eventData.projectId).toBe(VALID_UUID);
    expect(UUID_RE.test(eventData.projectId)).toBe(true);
  });
});
