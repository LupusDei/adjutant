// adj-141.3: UUID-only in proposal creation
// =============================================================================

describe("create_proposal UUID enforcement (adj-141.3)", () => {
  const SESSION_ID = "session-123";

  beforeEach(() => {
    vi.clearAllMocks();
    mockGetAgentBySession.mockReturnValue("test-agent");
    // resolveToolProjectContext delegates to getProjectContextBySession when no explicit projectId (adj-146)
    mockResolveToolProjectContext.mockImplementation((_explicitProjectId: string | undefined, sessionId: string | undefined) => {
      if (sessionId) return mockGetProjectContextBySession(sessionId);
      return undefined;
    });
  });

  it("should create proposal when projectContext.projectId is a valid UUID", async () => {
    mockGetProjectContextBySession.mockReturnValue({
      projectId: VALID_UUID,
      projectName: "adjutant",
      projectPath: "/code/adjutant",
      beadsDir: "/code/adjutant/.beads",
    });

    const store = createMockStore();
    const handler = getProposalToolHandler(store, "create_proposal");

    const result = await handler(
      { title: "Test", description: "A test", type: "engineering", project: "anything" },
      { sessionId: SESSION_ID },
    ) as { content: { text: string }[] };

    const parsed = JSON.parse(result.content[0].text);
    expect(parsed.error).toBeUndefined();
    expect(parsed.id).toBe("new-proposal-uuid");
    expect(store.insertProposal).toHaveBeenCalledWith(
      expect.objectContaining({ project: VALID_UUID }),
    );
  });

  it("should create proposal when projectContext.projectId is a truncated 8-char hex ID (adj-159)", async () => {
    mockGetProjectContextBySession.mockReturnValue({
      projectId: VALID_SHORT_ID,
      projectName: "adjutant",
      projectPath: "/code/adjutant",
      beadsDir: "/code/adjutant/.beads",
    });

    const store = createMockStore();
    const handler = getProposalToolHandler(store, "create_proposal");

    const result = await handler(
      { title: "Test", description: "A test", type: "engineering", project: "anything" },
      { sessionId: SESSION_ID },
    ) as { content: { text: string }[] };

    const parsed = JSON.parse(result.content[0].text);
    expect(parsed.error).toBeUndefined();
    expect(parsed.id).toBe("new-proposal-uuid");
    expect(store.insertProposal).toHaveBeenCalledWith(
      expect.objectContaining({ project: VALID_SHORT_ID }),
    );
  });

  it("should reject proposal creation when projectContext.projectId is a name string (not UUID)", async () => {
    mockGetProjectContextBySession.mockReturnValue({
      projectId: "adjutant",  // name, not UUID
      projectName: "adjutant",
      projectPath: "/code/adjutant",
      beadsDir: "/code/adjutant/.beads",
    });

    const store = createMockStore();
    const handler = getProposalToolHandler(store, "create_proposal");

    const result = await handler(
      { title: "Test", description: "A test", type: "engineering", project: "adjutant" },
      { sessionId: SESSION_ID },
    ) as { content: { text: string }[] };

    const parsed = JSON.parse(result.content[0].text);
    expect(parsed.error).toContain("Project ID must be a UUID");
    expect(store.insertProposal).not.toHaveBeenCalled();
  });

  it("should ignore client-supplied project and use server-resolved UUID", async () => {
    mockGetProjectContextBySession.mockReturnValue({
      projectId: VALID_UUID,
      projectName: "adjutant",
      projectPath: "/code/adjutant",
      beadsDir: "/code/adjutant/.beads",
    });

    const store = createMockStore();
    const handler = getProposalToolHandler(store, "create_proposal");

    // Client passes a name string — should be ignored in favor of UUID
    await handler(
      { title: "Test", description: "A test", type: "engineering", project: "client-supplied-name" },
      { sessionId: SESSION_ID },
    );

    expect(store.insertProposal).toHaveBeenCalledWith(
      expect.objectContaining({ project: VALID_UUID }),
    );
  });
});

